n = input('digite algo')
print(n.isnumeric())#para perguntar se é ukm numero
print(n.isalpha())#para pwerguntar se é letra
print(n.isalnum())#para perguntar se é letra ou número
print(n.isupper())#para perguntar se é só com letras maiusculas
print(n.isdecimal())#para verificar se é decimal 
print(n.isdigit())#para verificar se é um digito
print(n.islower())#para verificar se é apenas letras minusculas
print(n.isidentifier())#para verifcar se é um indentificador válido
print(n.isspace())#para verificar se tem espaço em branco
